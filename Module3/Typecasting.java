class Typecasting{
    public static void main(String args[]){
        double d=9.2;
        int x=(int)d;
        int y=10;
        double c=(double)y;
        System.out.println("Double value: " + d);
        System.out.println("After casting to int: " + x);

        System.out.println("Int value: " + y);
        System.out.println("After casting to double: " + c);
    }

}